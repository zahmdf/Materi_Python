# OPERASI ARITMATIKA


a = 30
b = 8

# Pertambahan (+)
Hasil = a + b
print(a, '+', b, '=', Hasil)

# Pengurangan (-)
Hasil = b - a
print(b, '-', a, '=', Hasil)

# Perkalian (*)
Hasil = a * b
print(a, '*', b, '=', Hasil)

# Pembagian (/)
Hasil = a / b
print(a, '/', b, '=', Hasil)

# Eksponen (**)
Hasil = a ** b
print(a, '**', b, '=', Hasil)

# Modulus (%)
Hasil = a % b
print(a, '%', b, '=', Hasil)

# Floor Division (//)
Hasil = a // b 
print(a, '//', b, '=', Hasil)

# prioritas operasi, operational precedence
'''
1. ()
2. eksponen **
3. perkalian dan kawan kawan
4. pertambahan dan pemgurangan
'''

x = 2
y = 4
z = 8

Hasil = y ** (z + x)* y / z - y % z
print(Hasil)
